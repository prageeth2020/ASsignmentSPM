package spm;

/**
 *
 * @author PIYUMI
 */
public class Subject {
    private int sid;
    private String academicYear;
    private String semester;
    private String program;
    private String groupNo;
    private String subGroupNo;

    /**
     * @return the sid
     */
    public int getSid() {
        return sid;
    }

    /**
     * @param sid the sid to set
     */
    public void setSid(int sid) {
        this.sid = sid;
    }

    /**
     * @return the academicYear
     */
    public String getAcademicYear() {
        return academicYear;
    }

    /**
     * @param academicYear the academicYear to set
     */
    public void setAcademicYear(String academicYear) {
        this.academicYear = academicYear;
    }

    /**
     * @return the semester
     */
    public String getSemester() {
        return semester;
    }

    /**
     * @param semester the semester to set
     */
    public void setSemester(String semester) {
        this.semester = semester;
    }

    /**
     * @return the program
     */
    public String getProgram() {
        return program;
    }

    /**
     * @param program the program to set
     */
    public void setProgram(String program) {
        this.program = program;
    }

    /**
     * @return the groupNo
     */
    public String getGroupNo() {
        return groupNo;
    }

    /**
     * @param groupNo the groupNo to set
     */
    public void setGroupNo(String groupNo) {
        this.groupNo = groupNo;
    }

    /**
     * @return the subGroupNo
     */
    public String getSubGroupNo() {
        return subGroupNo;
    }

    /**
     * @param subGroupNo the subGroupNo to set
     */
    public void setSubGroupNo(String subGroupNo) {
        this.subGroupNo = subGroupNo;
    }
    
    
}
