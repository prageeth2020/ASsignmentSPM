package spm;

/**
 *
 * @author PIYUMI
 */
public class NotAvailableTimesForGroups {
    private int id;
    private String group;
    private String on;
    private String at;

    /**
     * @return the id
     */
    public int getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(int id) {
        this.id = id;
    }

    /**
     * @return the group
     */
    public String getGroup() {
        return group;
    }

    /**
     * @param group the group to set
     */
    public void setGroup(String group) {
        this.group = group;
    }

    /**
     * @return the on
     */
    public String getOn() {
        return on;
    }

    /**
     * @param on the on to set
     */
    public void setOn(String on) {
        this.on = on;
    }

    /**
     * @return the at
     */
    public String getAt() {
        return at;
    }

    /**
     * @param at the at to set
     */
    public void setAt(String at) {
        this.at = at;
    }

}
