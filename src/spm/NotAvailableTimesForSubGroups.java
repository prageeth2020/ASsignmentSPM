package spm;

/**
 *
 * @author PIYUMI
 */
public class NotAvailableTimesForSubGroups {
    private int id;
    private String subGroup;
    private String on;
    private String at;

    /**
     * @return the id
     */
    public int getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(int id) {
        this.id = id;
    }

    /**
     * @return the subGroup
     */
    public String getSubGroup() {
        return subGroup;
    }

    /**
     * @param subGroup the subGroup to set
     */
    public void setSubGroup(String subGroup) {
        this.subGroup = subGroup;
    }

    /**
     * @return the on
     */
    public String getOn() {
        return on;
    }

    /**
     * @param on the on to set
     */
    public void setOn(String on) {
        this.on = on;
    }

    /**
     * @return the at
     */
    public String getAt() {
        return at;
    }

    /**
     * @param at the at to set
     */
    public void setAt(String at) {
        this.at = at;
    }
}
